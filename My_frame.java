    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jLabel1.setText("Кол-во попаданий: "+String.valueOf(hits));
        jLabel2.setText("Кол-во выстрелов: "+String.valueOf(shoots));
        if(t!=null){
            return;
        }        
        t=new Thread(()->{
            run=true;
            while(run){            
                    
                    try {
                        panel_my1.nextyOV1();
                        
                        if(pause)
                        {   
                            synchronized(this)
                            {            
                                wait();
                                pause = false;
                            }
                        }
                        Thread.sleep(100);
                    }
                    catch (InterruptedException ignored) {
                        run = false;
                    }   
            }
              
        });
        t.start();
        if(t2!=null){
            return;
        }        
        t2=new Thread(()->{
            run=true;
            while(run){            
                    
                    try {
                        panel_my1.nextyOV2();
                        
                        if(pause)
                        {   
                            synchronized(this)
                            {            
                                wait();
                                pause = false;
                            }
                        }
                        Thread.sleep(100);
                    }
                    catch (InterruptedException ex) {
                        run = false;
                    }   
            }
              
        });
        t2.start();
    }//GEN-LAST:event_jButton1ActionPerformed

