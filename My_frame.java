    public void nextx() {
        
       int  newx1=panel_my1.x1+5;
       
        int newx2=panel_my1.x2+5;
        
        int newx3=panel_my1.x3+5;
        
        int newx4=panel_my1.endx4+5;
        
        if(panel_my1.quad(panel_my1.xOV2-newx4)+panel_my1.quad(panel_my1.yOV2-panel_my1.endy4)<=(panel_my1.r*4-12)*(panel_my1.r*4-12)){
           
            hits+=1;
            newx1=0;
            newx2=25;
            newx3=25;
            newx4=30;
            pause=true;
        }
        else if(panel_my1.quad(panel_my1.xOV-newx4)+panel_my1.quad(panel_my1.yOV-panel_my1.endy4)<=(panel_my1.r*2-5)*(panel_my1.r*2-5)){
            hits+=2;
            newx1=0;
            newx2=25;
            newx3=25;
            newx4=30;
            pause=true;
        }
        else if(newx4>panel_my1.getWidth()){
            newx1=0;
            newx2=25;
            newx3=25;
            newx4=30;
             pause=true;
        }
        
      
        panel_my1.myArrow(newx1,newx2,newx3,newx4,panel_my1.y1,panel_my1.y2,panel_my1.y3,panel_my1.endy4);
        
    }

    
    
